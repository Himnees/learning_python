# Comparison Operator
m = 10
n = 20
a = [1,2,3,4,5,6]
b = [1,2,3,4,5,6]
c = [1,2,3,4,5,6,7]
u = m == n
v = m != n
w = a == b
x = a == c
y = m > n
z = m < n
l = a > b
o = a < c
print(" u is",u)
print(" v is",v)
print(" w is",w)
print(" x is",x)
print(" y is",y)
print(" z is",z)
print(" l is",l)
print(" o is",o)